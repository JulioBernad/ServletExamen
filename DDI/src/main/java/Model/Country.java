package Model;

public class Country {
	private String name;
	private String language;
	
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String lengua) {
		this.language = lengua;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
